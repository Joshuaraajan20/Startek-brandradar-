// Get aggregated monthly data
export const getMonthlyAggregatedData = (month) => {
  const weeksInMonth = getWeeksForMonth(month);
  
  const aggregated = {
    month,
    weekCount: weeksInMonth.length,
    weeks: weeksInMonth,
    companies: {}
  };

  // Initialize company aggregations
  COMPANY_ORDER.forEach(key => {
    aggregated.companies[key] = {
      avgDA: 0,
      totalPosts: 0,
      totalAds: 0,
      totalPages: 0,
      insights: []
    };
  });

  // Aggregate data
  weeksInMonth.forEach(week => {
    COMPANY_ORDER.forEach(key => {
      const data = week.companies[key];
      aggregated.companies[key].avgDA += data.da;
      aggregated.companies[key].totalPosts += data.posts;
      aggregated.companies[key].totalAds += data.ads;
      aggregated.companies[key].totalPages += data.pages;
      if (data.insight && data.insight !== 'No significant activity.') {
        aggregated.companies[key].insights.push({
          week: week.week,
          date: week.date,
          insight: data.insight
        });
      }
    });
  });

  // Calculate averages
  COMPANY_ORDER.forEach(key => {
    aggregated.companies[key].avgDA = Math.round((aggregated.companies[key].avgDA / weeksInMonth.length) * 10) / 10;
  });

  // Collect all decks from the month
  aggregated.decks = weeksInMonth
    .filter(week => week.decks)
    .map(week => ({
      week: week.week,
      date: week.date,
      links: week.decks
    }));

  return aggregated;
};

